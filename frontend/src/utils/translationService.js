/**
 * Hook to get the translation function for the currently selected language.
 * Falls back to English if a key is missing in the selected language.
 */
export function useTranslation() {
  const { preferences } = useHealth()
  const lang = preferences?.language || 'English'
  const dict = translations[lang] || translations['English']
  const fallback = translations['English']

  const t = (key) => {
    return dict[key] ?? fallback[key] ?? key
  }

  return { t, lang }
}

export default translations
